nombre = 'jonathan'
print(len(nombre)) #longitud de la cadena o variable
print(nombre.find('a')) #encontrar la posicion de algun caracter
print(nombre.capitalize()) #mayuscula la primera letra
print(nombre.upper())#hace mayuscula todo
print(nombre.lower())#minuscula todo el texto
print(nombre.isdigit()) #Si es uun digito o no(true o false)
print(nombre.isalpha()) #solo caracteres alfabeticos (true o false)
print(nombre.count('a')) #contar cuantos caracteres hay/diferencia entre mayus y minusculas
print(nombre.replace('a','o')) #remplazar caracteres
print(nombre*2) #mostrar dos veces la cadena

